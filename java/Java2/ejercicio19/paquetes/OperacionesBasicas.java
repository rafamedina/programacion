package paquetes;

public class OperacionesBasicas  {
    public void sumar(int a, int b) {
	int resultado = a + b;
	System.out.println(resultado); }


public void restar(int a, int b) {
	int resultado = a - b;
	System.out.println(resultado); }

public void multiplicar(int a, int b) {
	int resultado = a * b;
	System.out.println(resultado); }

public void dividir(int a, int b) {
	int resultado = a / b;
	System.out.println(resultado); }}